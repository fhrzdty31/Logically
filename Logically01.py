# Mana yang lebi besar

print("\n\n== Lebih Besar Lebih Kecil ==\n\n")
a = int(input('Masukkan Nilai A : '))
b = int(input('Masukkan Nilai B : '))

if a > b :
    print(f'\n=> {a} lebih besar dari {b}\n')
elif a < b :
    print(f'\n=> {b} lebih besar dari {a}\n')
else :
    print(f'=> {a} sama dengan {b}\n')